a = int(input("Bir a değeri girin."))
b = int(input("Bir b değeri girin."))

if a==b:
    print("a değeri", a, "b değeri", b, "a değeri b değerine eşittir.")
elif a>b:
    print("a değeri=", a, "b değeri=", b, "a değeri b değerinden büyüktür.")
else:
    print("a değeri=", a, "b değeri=", b, "a değeri b değerinden küçüktür.")
